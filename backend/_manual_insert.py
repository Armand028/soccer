def parse_timestamp(ts_str):
    if not ts_str:
        return None
    try:
        dt = datetime.datetime.strptime(ts_str, "%Y-%m-%d %H:%M:%S")
        return int(dt.timestamp())
    except (ValueError, TypeError):
        return None

def upsert_match(cursor, ev):
    event_id = int(ev['idEvent'])
    kickoff = parse_timestamp(ev['strTimestamp'])
    home_score = int(ev['Home Score']) if ev['Home Score'] else -1
    away_score = int(ev['Away Score']) if ev['Away Score'] else -1
    date_str = ev['strTimestamp'].split(' ')[0] if ev['strTimestamp'] else None

    cursor.execute("SELECT id FROM matches WHERE event_id = ?", (event_id,))
    existing = cursor.fetchone()
    if existing:
        cursor.execute("""
            UPDATE matches SET home_score=?, away_score=?, kickoff_timestamp=?
            WHERE event_id=? AND (home_score != ? OR away_score != ?)
        """, (home_score, away_score, kickoff, event_id, home_score, away_score))
        return "updated" if cursor.rowcount > 0 else "unchanged"

    cursor.execute("""
        INSERT OR IGNORE INTO matches
        (league, season, round, date, home_team, away_team, home_score, away_score,
         event_id, status, kickoff_timestamp)
        VALUES (?,?,?,?,?,?,?,?,?,?,?)
    """, ('FA Cup', '2025-2026', ev['Round'], date_str,
          ev['Home Team'], ev['Away Team'], home_score, away_score,
          event_id, 'finished' if home_score != -1 else 'notstarted', kickoff))
    return "inserted" if cursor.rowcount > 0 else "skipped"

